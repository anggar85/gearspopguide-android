package com.mxlapps.app.gearspopguide.Model;

public class TierDataModel {

    private String overall;
    private String pvp;
    private String pve;
    private String lab;
    private String wrizz;
    private String soren;

    public String getOverall() {
        return overall;
    }

    public void setOverall(String overall) {
        this.overall = overall;
    }

    public String getPvp() {
        return pvp;
    }

    public void setPvp(String pvp) {
        this.pvp = pvp;
    }

    public String getPve() {
        return pve;
    }

    public void setPve(String pve) {
        this.pve = pve;
    }

    public String getLab() {
        return lab;
    }

    public void setLab(String lab) {
        this.lab = lab;
    }

    public String getWrizz() {
        return wrizz;
    }

    public void setWrizz(String wrizz) {
        this.wrizz = wrizz;
    }

    public String getSoren() {
        return soren;
    }

    public void setSoren(String soren) {
        this.soren = soren;
    }
}
